/* ─── Merge helpers ──────────────────────────────────────────────── */

function applyTile(base: BriefTile, loc: LocalizedTile | undefined): BriefTile {
  if (!loc) return base;
  switch (base.kind) {
    case "stat":
      return { ...base, label: loc.label ?? base.label };
    case "label":
      return { ...base, key: loc.key ?? base.key, value: loc.value ?? base.value };
    case "quote":
      return { ...base, text: loc.text ?? base.text, cite: loc.cite ?? base.cite };
    case "tags":
      return { ...base, label: loc.label ?? base.label, items: loc.items ?? base.items };
  }
}

