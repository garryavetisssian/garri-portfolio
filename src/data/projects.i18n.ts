export const PROJECT_CONTENT_I18N: Record<string, PerLocale> = (() => {
  const map: Record<string, PerLocale> = {};
  for (const slug of new Set([...Object.keys(ru), ...Object.keys(hy)])) {
    map[slug] = { ru: ru[slug], hy: hy[slug] };
  }
  return map;
})();

